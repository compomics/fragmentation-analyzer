    /**
     * Creates a new HelpWindow object with a Frame as a parent.
     *
     * @param parent
     * @param fileName the name of the help file
     */
    public HelpWindow(java.awt.Frame parent, URL fileName) {

        // only works for Java 1.6 and newer
        //this.setModalExclusionType(ModalExclusionType.APPLICATION_EXCLUDE);
        initComponents();

        try {
            InputStream stream = fileName.openStream();
            InputStreamReader streamReader = new InputStreamReader(stream);
            BufferedReader b = new BufferedReader(streamReader);
            String s = b.readLine();
            String helpText = "";

            while (s != null) {
                helpText += s;
                s = b.readLine();
            }

            textJEditorPane.setText(helpText);

            if (fileName.getPath().endsWith("AboutFragmentationAnalyzer.html")) {
                setTitle("About");
                setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().
                        getResource("/no/uib/fragmentation_analyzer/icons/box_plot_small.GIF")));
            } else {
                setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().
                        getResource("/no/uib/fragmentation_analyzer/icons/help.GIF")));
            }
        } catch (Exception e) {

            try {
                textJEditorPane.setPage(getClass().getResource(
                        "/no/uib/fragmentation_analyzer/helpfiles/DefaultHelpFile.html"));
            } catch (Exception ex) {
                textJEditorPane.setText("The selected help file is not yet available.");
            }
        }

        textJEditorPane.setCaretPosition(0);

        setSize(480, 500);
        setLocationRelativeTo(parent);
        setVisible(true);
    }

